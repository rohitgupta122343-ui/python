
import numpy as np 

arr = np.array([1,2,3,4,5])

# ---------- Mathematical Functions ----------


print("Original Array:", arr)
 

print("Maximum no of array: ",np.max(arr))
print("Minimum no of array: ", np.min(arr))
print("Average no of array: ",np.mean(arr))
print("Sum no of array: ", np.sum(arr))
print("Square of array: ",np.square(arr))
print("Sin (in radians): ",np.sin(arr))
print("Cos (in radians): ", np.cos(arr))