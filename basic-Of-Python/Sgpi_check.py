
sgpi = float(input("enter a sgpi : "))

if 9.00 <= sgpi <= 10.00:
    print("O Grade ")

elif 8.00 <= sgpi <= 8.99:
    print("A+ Grade ")

elif 7.00 <= sgpi <= 7.99:
    print("A Grade ")        

elif 6.00 <= sgpi <= 6.99:
    print("B+ Grade ")        

elif 5.50 <= sgpi <= 5.99:
    print("b Grade ") 

elif 5.00 <= sgpi <= 5.49:
    print("b Grade ")           

elif 4.00 <= sgpi <= 4.99:
    print("Pass ")   

else:
    print("Fail ")